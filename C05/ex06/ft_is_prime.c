int	ft_is_prime(int nb)
{
	int	n;
	int	in;

	if (nb <= 1 || nb == 4)
		return (0);
	else if ((nb % 2 == 0 || nb % 3 == 0 || nb % 5 == 0) && nb > 5)
		return (0);
	n = 3;
	in = nb / 2;
	while (n <= in)
	{
		if (nb % n == 0 || nb % n == 0)
			return (0);
		n += 2;
		in -= 2;
	}
	return (1);
}
