#include <unistd.h>

int	main(int argc, char **args)
{
	int	i;
	int	n;

	n = argc - 1;
	if (argc > 1)
	{
		while (n >= 1)
		{
			i = 0;
			while (args[n][i])
				i++;
			write(1, args[n], i);
			write(1, "\n", 1);
			n--;
		}
	}
}
