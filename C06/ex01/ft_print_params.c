#include <unistd.h>

int	main(int size, char **arg)
{
	int	i;
	int	n;

	n = 1;
	if (size > 1)
	{
		while (n < size)
		{
			i = 0;
			while (arg[n][i])
				i++;
			write(1, arg[n], i);
			write(1, "\n", 1);
			n++;
		}
	}
	return (0);
}
